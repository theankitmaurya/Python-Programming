# To create a tuple we use round brackets i.e. ().
# To know the python interpreter that the given data is a tuple we use a comma at the end of the data.
# Tuples are immutable.

a = (1, 34, 7652, False, "Shivam", "YouTube")

print(a) # returns (1, 34, 7652, False, 'Shivam', 'YouTube')
print(type(a)) # returns <class 'tuple'>

